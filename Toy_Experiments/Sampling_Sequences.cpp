void build_DDT() {
	for (int i = 0; i < 256; i++) {
		for (int j = i + 1; j < 256; j++) {
			DDT[i ^ j][Sbox[i] ^ Sbox[j]].push_back(i);
			DDT[i ^ j][Sbox[i] ^ Sbox[j]].push_back(j);
		}
	}
}
int MUL(int a, int b) {
	int ret = 0;
	for (int i = 0; i < 8; i++) {
		if ((b >> i) & 1) ret ^= (a << i);
	}
	for (int i = 14; i >= 8; i--) {
		if ((ret >> i) & 1) ret ^= (0x11b << (i - 8));
	}

	return ret;
}
